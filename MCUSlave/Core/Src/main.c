void HAL_I2C_AddrCallback(I2C_HandleTypeDef *hi2c, uint8_t TransferDirection, uint16_t AddrMatchCode)
{
	if (TransferDirection == I2C_DIRECTION_TRANSMIT)
	{
		HAL_I2C_Slave_Seq_Receive_IT(hi2c, receive_buffer, 32, I2C_FIRST_FRAME);
	}
	else
	{
		switch (command)
		{
		case RTCRequest:
		{
			RTC_TimeTypeDef time = {0};
			RTC_DateTypeDef date = {0};
		    if (HAL_RTC_GetTime(&hrtc, &time, RTC_FORMAT_BIN) == HAL_OK &&
		        HAL_RTC_GetDate(&hrtc, &date, RTC_FORMAT_BIN) == HAL_OK)
		    {
		    	send_data[0] = date.Year;
		    	send_data[1] = date.Month;
		    	send_data[2] = date.Date;
		    	send_data[3] = date.WeekDay;
		    	send_data[4] = time.Hours;
		    	send_data[5] = time.Minutes;
		    	send_data[6] = time.Seconds;
		    	HAL_I2C_Slave_Seq_Transmit_IT(hi2c, send_data, 7, I2C_LAST_FRAME);
		    }

			break;
		}
		case CPUTempRequest:
		{
			uint8_t * p_temperature = (uint8_t*)(&cpu_temperature);
			send_data[0] = p_temperature[0];
			send_data[1] = p_temperature[1];
			send_data[2] = p_temperature[2];
			send_data[3] = p_temperature[3];
			HAL_I2C_Slave_Seq_Transmit_IT(hi2c, send_data, 4, I2C_LAST_FRAME);
			break;
		}
		default:
			break;
		}

	}
}

