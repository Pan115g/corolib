void HAL_I2C_SlaveRxCpltCallback(I2C_HandleTypeDef *hi2c)
{
	command = receive_buffer[0];
	switch (command)
	{
	case RTCRequest:
	case CPUTempRequest:
		break;
	case ResetRTC:
		resetRTC(&receive_buffer[1]);
		break;
	default:
		break;
	}
	HAL_I2C_EnableListen_IT(&hi2c2);
}

void startADCConversion()
{
	HAL_ADC_Start_DMA(&hadc1, (uint32_t*)adcValues, hadc1.Init.NbrOfConversion);
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc)
{
	float temperature = 0;
    for (int i = 0; i < hadc->Init.NbrOfConversion; ++i)
    {
    	temperature += (((float)adcValues[i] * 3.3/4096.0) - 0.76)/2.5 + 25.0;
    }
    cpu_temperature = temperature / (float)hadc->Init.NbrOfConversion;
}

